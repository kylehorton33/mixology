<h1>Negroni</h1>
<p>
    INGREDIENTS
    
    30 ml Gin
    30 ml Bitter Campari
    30 ml Sweet Red Vermouth
    METHOD
    
    Pour all ingredients directly into a chilled old fashioned glass filled with ice, Stir gently.
    GARNISH
    
    Garnish with a half orange slice.
     </p>

